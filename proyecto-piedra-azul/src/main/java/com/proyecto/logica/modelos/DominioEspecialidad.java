package com.proyecto.logica.modelos;

public class DominioEspecialidad {

    private int id;
    private String nombre;

    public DominioEspecialidad() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}